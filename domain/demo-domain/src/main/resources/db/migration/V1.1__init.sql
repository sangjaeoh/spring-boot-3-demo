create table users (
    age integer,
    id bigint generated by default as identity,
    email varchar(255) not null,
    name varchar(255),
    password varchar(255) not null,
    primary key (id)
);

create table authorities (
    id bigint generated by default as identity,
    role enum ('ADMIN','COMMON') not null unique,
    primary key (id)
);

create table users_authorities (
    authority_id bigint,
    id bigint generated by default as identity,
    user_id bigint,
    primary key (id)
);

